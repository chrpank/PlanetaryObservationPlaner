#include "scenario_functions.h"

#include <acutest.h>
#include <math.h>

typedef struct
{
	float hour;
	float minute;
	float second;
	int day;
	int month;
	int year;
	float azi;
	float alt;
} obsdata;

int test_azimuthal_coordinates(const char* object, const obsdata data[], const int data_size)
{
	float azi, alt;
	float lon = 13.0;
	float lat = 52.0;

	for(int index = 0; index < data_size; index++)
	{
		TEST_CHECK(scenario_calculate_azimuthal_coordinates(
					   object,
					   data[index].year,
					   data[index].month,
					   data[index].day,
					   data[index].hour + data[index].minute / 60.0 + data[index].second / 3600.0,
					   lon,
					   lat,
					   &azi,
					   &alt) != 0);

		if(fabsf(data[index].azi - azi) > 1.0 || fabsf(data[index].alt - alt) > 1.0)
		{
			return 0;
		}
	}

	return 1;
}

